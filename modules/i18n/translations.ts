export function getStatusLabel(status: string, locale: Locale | string): string {
  const resolved = resolveLocale(locale);
  return statusLabels[status as keyof typeof statusLabels]?.[resolved] ?? status;
}

const aliases = [...entries, ...Object.values(statusLabels)]
  .flatMap((entry) => supportedLocales.map((locale) => [entry[locale], entry] as const))
  .sort(([a], [b]) => b.length - a.length);

const aliasMap = new Map(aliases);
const aliasPattern = new RegExp(
  `(?<![\\p{L}\\p{N}])(${aliases.map(([source]) => source.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")).join("|")})(?![\\p{L}\\p{N}])`,
  "gu",
);

export function translateText(text: string, locale: Locale | string): string {
  const resolved = resolveLocale(locale);
  const withoutInternalStatuses = Object.keys(statusLabels).reduce(
    (result, status) =>
      result.replaceAll(status, statusLabels[status as keyof typeof statusLabels][resolved]),
    text,
  );
  return withoutInternalStatuses.replace(aliasPattern, (source) => aliasMap.get(source)?.[resolved] ?? source);
}

export function translateBusinessText(text: string, locale: Locale | string): string {
  const resolved = resolveLocale(locale);
  const clarifiedTerms = text.replaceAll(
    "pregovarajte o nižem MOQ-u",
    resolved === "de"
      ? "verhandeln Sie eine niedrigere Mindestbestellmenge (MOQ)"
      : resolved === "en"
        ? "negotiate a lower minimum order quantity (MOQ)"
        : "pregovarajte o nižoj minimalnoj količini (MOQ)",
  );
  if (resolved === "sr") return translateText(clarifiedTerms, resolved);

  const withMoqWarnings = clarifiedTerms.replace(
    /Tražite (\d+) kom, dobavljač traži minimum (\d+) kom\./g,
    resolved === "de"
      ? "Sie suchen $1 Stk., der Lieferant verlangt mindestens $2 Stk."
      : "You need $1 units, the supplier requires a minimum of $2 units.",
  );

  const decisionComparison = withMoqWarnings.replace(
    /Imate (\d+) ponuda, ali (\d+) su direktno uporedive u valuti ([^.]+)\./g,
    resolved === "de"
      ? "Sie haben $1 Angebote, aber nur $2 sind in der Währung $3 direkt vergleichbar."
      : "You have $1 offers, but only $2 are directly comparable in $3.",
  ).replace(
    /Imate (\d+) ponuda i sve su uporedive u valuti ([^.]+)\./g,
    resolved === "de"
      ? "Sie haben $1 Angebote und alle sind in der Währung $2 vergleichbar."
      : "You have $1 offers and all are comparable in $2.",
  ).replace(
    /Najbolja ukupna ponuda je (.+?) sa ocenom (\d+)\/100\./g,
    resolved === "de"
      ? "Das beste Gesamtangebot ist $1 mit einer Bewertung von $2/100."
      : "The best overall offer is $1 with a score of $2/100.",
  ).replace(
    /Još nema dovoljno ocenjenih ponuda za izbor najbolje ponude\./g,
    resolved === "de"
      ? "Es gibt noch nicht genügend bewertete Angebote, um das beste auszuwählen."
      : "There are not enough assessed offers yet to select the best one.",
  );

  return translateText(decisionComparison, resolved);
}
